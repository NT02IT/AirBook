    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
    String priceText = txtPrice.getText();
    String luggageWeight = (String) cbLuggageWeight.getSelectedItem();
    if (priceText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin hành lý");
        return;
    }

    try {
        moreLuggageBUS = new MoreLuggageBUS();
        int price = Integer.valueOf(priceText);
        int weight = Integer.valueOf(luggageWeight);                        
        MoreLuggage moreLuggage = new MoreLuggage();
        moreLuggage.setAirlineID(airlineID);
        moreLuggage.setPrice(price);
        moreLuggage.setLuggageWeight(weight);
        moreLuggage.setIsDelete(0);
        
        // Gọi hàm create để thêm hành lý vào cơ sở dữ liệu
        boolean success = moreLuggageBUS.create(moreLuggage);

        if (success) {
            JOptionPane.showMessageDialog(this, "Thêm hành lý thành công");
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Thêm hành lý thất bại");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập giá thành hợp lệ");
    } catch (ClassNotFoundException | SQLException | IOException ex) {
        Logger.getLogger(PuPlaneAD.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_btSaveActionPerformed

